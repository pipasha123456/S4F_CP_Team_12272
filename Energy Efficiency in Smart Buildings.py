# ==========================================================
plt.figure(figsize=(10, 8))
sns.set(style="whitegrid")

# Compute correlation matrix for numeric columns
correlation_matrix = df.corr(numeric_only=True)

# Plot heatmap
sns.heatmap(correlation_matrix, annot=True, cmap='coolwarm', fmt=".2f", square=True, linewidths=0.5)
plt.title('Correlation Heatmap - Smart Building Features')
plt.tight_layout()
plt.show()

# ==========================================================
# ðŸ“ˆ 2. Scatter Plot: Wall Area vs. Heating Load
# ==========================================================
# Check if required columns are in the dataset
if 'Wall Area' in df.columns and 'Heating Load' in df.columns:
    plt.figure(figsize=(8, 6))
    sns.scatterplot(data=df, x='Wall Area', y='Heating Load')
    plt.title('Wall Area vs. Heating Load')
    plt.xlabel('Wall Area')
    plt.ylabel('Heating Load')
    plt.grid(True)
    plt.tight_layout()
    plt.show()